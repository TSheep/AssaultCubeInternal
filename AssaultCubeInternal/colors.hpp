struct RGBA
{
	int R, G, B, A;
};

static __forceinline RGBA DWORD2RGBA(unsigned long dwColor)
{
	RGBA tmp; /* why did you declare it static??? */


	tmp.A = (dwColor >> 24);
	tmp.R = (dwColor >> 16) & 255;
	tmp.G = (dwColor >> 8) & 255;
	tmp.B = (dwColor & 255);

	return tmp;
}